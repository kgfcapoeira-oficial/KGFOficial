export type TranslationKey = keyof typeof translations.pt;

export function getTranslation(lang: Language, key: TranslationKey, params?: Record<string, any>): string {
  const langDict = (translations[lang] ?? translations.pt) as Record<string, string>;
  let val = langDict[key] ?? (translations.pt as Record<string, string>)[key] ?? key;
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      val = val.replace(`{${k}}`, String(v));
    });
  }
  return val;
}

